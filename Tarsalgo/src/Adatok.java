
public class Adatok {
	private int ora;
	private int perc;
	private int azonosito;
	private boolean kibe;
	public int getOra() {
		return ora;
	}
	public void setOra(int ora) {
		this.ora = ora;
	}
	public int getPerc() {
		return perc;
	}
	public void setPerc(int perc) {
		this.perc = perc;
	}
	public int getAzonosito() {
		return azonosito;
	}
	public void setAzonosito(int azonosito) {
		this.azonosito = azonosito;
	}
	public boolean isKibe() {
		return kibe;
	}
	public void setKibe(boolean kibe) {
		this.kibe = kibe;
	}
}
